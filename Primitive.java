public class Primitive {

    public static void main(String[] args) {

        // 1. byte (1 byte)
        byte age = 21;
        System.out.println("Byte value (age): " + age);

        // 2. short (2 bytes)
        short year = 2025;
        System.out.println("Short value (year): " + year);

        // 3. int (4 bytes)
        int salary = 50000;
        System.out.println("Int value (salary): " + salary);

        // 4. long (8 bytes)
        long population = 1400000000;
        System.out.println("Long value (population): " + population);

        // 5. float (4 bytes)
        float percentage = 85.5f;
        System.out.println("Float value (percentage): " + percentage);

        // 6. double (8 bytes)
        double pi = 3.14159265359;
        System.out.println("Double value (pi): " + pi);

        // 7. char (2 bytes)
        char grade = 'A';
        System.out.println("Char value (grade): " + grade);

        // 8. boolean (1 bit)
        boolean isPassed = true;
        System.out.println("Boolean value (isPassed): " + isPassed);
    }
}
